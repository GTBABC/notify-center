package com.gtbabc.notifycenter.core.constant;

/**
 * 通知等级枚举
 * `NotifyLevel` 用于定义通知的严重程度或优先级。不同的通知等级决定了通知的紧急程度，
 * 从普通的信息通知到高优先级的告警通知。
 */
public enum NotifyLevel {

    /**
     * 普通通知，用于信息类的通知，表示无需特别关注的通知
     * 例如：系统状态检查、操作成功等普通信息
     */
    INFO,

    /**
     * 提醒通知，表示需要关注但不紧急的通知
     * 例如：账户快到期、任务即将到期的提醒等
     */
    REMIND,

    /**
     * 告警通知，表示重要通知，需要立即处理或关注
     * 例如：订单超时、系统异常等需要及时响应的事件
     */
    ALERT,

    /**
     * 严重告警通知，表示极为重要的通知，需要立即处理
     * 例如：系统宕机、关键业务失败等，需要马上采取行动的事件
     */
    CRITICAL
}

